import { NativeStackScreenProps } from "@react-navigation/native-stack";
import * as Haptics from "expo-haptics";
import { useState } from "react";
import { Text, ToastAndroid, View } from "react-native";
import {
  useAnimatedStyle,
  useSharedValue,
  withRepeat,
  withSequence,
  withTiming,
} from "react-native-reanimated";
import Svg, { SvgProps, G, Path, Defs, ClipPath } from "react-native-svg";
import { RootStackParamList } from "./root-stack-param-list";
import { ScreenLayout } from "./screen-layout";
import { useLang } from "@/modules/lang";
import { useChangeVaultPassword } from "@/modules/vault";
import {
  ScalablePressable,
  useTheme,
  RightArrowIcon,
  EASING,
  PasswordInput,
} from "@/ui";

export function ChangeVaultPasswordScreen({
  navigation,
}: NativeStackScreenProps<RootStackParamList, "ChangeVaultPassword">) {
  const { colors, scale } = useTheme();
  const lang = useLang();

  const changeVaultPassword = useChangeVaultPassword();

  const [password, setPassword] = useState("");

  const changeVaultPasswordButtonTranslateX = useSharedValue(0);
  const changeVaultPasswordButtonTranslateXAnimatedStyle = useAnimatedStyle(
    () => {
      return {
        transform: [
          {
            translateX: changeVaultPasswordButtonTranslateX.value,
          },
        ],
      };
    },
  );

  const shakeChangeVaultPasswordButton = () => {
    const offset = 2;
    const duration = 50;
    changeVaultPasswordButtonTranslateX.value = withSequence(
      withTiming(offset, {
        duration: duration,
        easing: EASING,
      }),
      withRepeat(
        withTiming(-offset, {
          duration,
          easing: EASING,
        }),
        2,
        true,
      ),
      withTiming(0, { duration: duration * 3, easing: EASING }),
    );
    Haptics.notificationAsync(Haptics.NotificationFeedbackType.Error);
  };

  const handleChangeVaultPasswordPress = async () => {
    if (password) {
      await changeVaultPassword(password);

      ToastAndroid.show(
        lang.changeVaultPasswordScreen.successfullyChanged,
        ToastAndroid.SHORT,
      );
      navigation.reset({
        index: 0,
        routes: [{ name: "MyPasswords" }],
      });
    } else {
      shakeChangeVaultPasswordButton();
      Haptics.notificationAsync(Haptics.NotificationFeedbackType.Error);
      ToastAndroid.show(
        lang.changeVaultPasswordScreen.passwordEmptyError,
        ToastAndroid.SHORT,
      );
    }
  };

  return (
    <ScreenLayout
      header={{
        separator: true,
        backButton: true,
      }}
    >
      <View
        style={{
          flex: 1,
          justifyContent: "center",
          alignItems: "center",
          gap: scale(32),
          width: "100%",
        }}
      >
        <Illustration />
        <View
          style={{
            alignItems: "center",
            gap: scale(22),
            width: "100%",
          }}
        >
          <View
            style={{
              alignItems: "center",
              gap: scale(12),
            }}
          >
            <Text
              style={{
                fontFamily: "Gilroy-Bold",
                fontSize: scale(24),
                color: colors.text,
              }}
            >
              {lang.changeVaultPasswordScreen.title}
            </Text>
            <Text
              style={{
                fontFamily: "Gilroy-Medium",
                fontSize: scale(14),
                lineHeight: scale(19.6),
                textAlign: "center",
                color: colors.subtext,
              }}
            >
              {lang.changeVaultPasswordScreen.descriptionFirstLine}
              {"\n"}
              {lang.changeVaultPasswordScreen.descriptionSecondLine}
            </Text>
          </View>
          <View
            style={{
              flexDirection: "row",
              gap: scale(10),
              width: "100%",
            }}
          >
            <PasswordInput
              value={password}
              onChangeText={setPassword}
              containerStyle={{
                flex: 1,
              }}
              placeholder={lang.passwordInput.placeholder}
            />
            <ScalablePressable
              style={[
                {
                  borderRadius: scale(16),
                  padding: scale(13.9),
                  backgroundColor: colors.text,
                },
                changeVaultPasswordButtonTranslateXAnimatedStyle,
              ]}
              onPress={handleChangeVaultPasswordPress}
            >
              <RightArrowIcon color={colors.primary} size="md" />
            </ScalablePressable>
          </View>
        </View>
      </View>
    </ScreenLayout>
  );
}

